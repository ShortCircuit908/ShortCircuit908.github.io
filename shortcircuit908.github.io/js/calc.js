function Calcugram(canvas_element, x_scale, y_scale) {
    this.x_scale = x_scale || canvas_element.clientWidth / 95;
    this.y_scale = y_scale || canvas_element.clientHeight / 63;
    this.g = canvas_element.getContext("2d");
}

Calcugram.prototype.x_scale = 1;
Calcugram.prototype.y_scale = 1;

Calcugram.prototype.g = undefined;

Calcugram.prototype.setPixel = function (x, y, state) {
    x = Math.round(x);
    y = 62 - Math.round(y);
    if (state) {
        this.g.fillStyle = "rgba(0, 0, 0, 255)";
        this.g.fillRect(x * this.x_scale, y * this.y_scale, this.x_scale, this.y_scale);
    }
    else {
        this.g.clearRect(x * this.x_scale, y * this.y_scale, this.x_scale, this.y_scale);
    }
};

Calcugram.prototype.testPixel = function (x, y) {
    x = Math.round(x);
    y = Math.round(y);
    var data = this.g.getImageData(x * this.x_scale, y * this.y_scale, 1, 1);
    return data.data[3] != 0;
};

Calcugram.prototype.drawLine = function (x0, y0, x1, y1) {
    if (!isFinite(x0) || !isFinite(x1) || !isFinite(y0) || !isFinite(y1)) {
        return;
    }
    var temp;
    if (x0 == x1) {
        if (y1 < y0) {
            temp = y1;
            y1 = y0;
            y0 = temp;
        }
        for (var y = y0; y <= y1; y++) {
            this.setPixel(x0, y, true, true);
        }
        return;
    }

    if (x1 > x0) {
        temp = x1;
        x1 = x0;
        x0 = temp;
        temp = y1;
        y1 = y0;
        y0 = temp;
    }

    var dx = Math.abs(x1 - x0);
    var dy = Math.abs(y1 - y0);
    var sx = (x0 < x1) ? 1 : -1;
    var sy = (y0 < y1) ? 1 : -1;
    var err = dx - dy;

    while (true) {
        this.setPixel(x0, y0, true, true);
        if ((x0 == x1) && (y0 == y1)) {
            break;
        }
        var e2 = 2 * err;
        if (e2 > -dy) {
            err -= dy;
            x0 += sx;
        }
        if (e2 < dx) {
            err += dx;
            y0 += sy;
        }
    }
};

Calcugram.prototype.clear = function () {
    this.g.clearRect(0, 0, 95 * this.x_scale, 63 * this.y_scale);
};

Calcugram.prototype.drawEquation = function (func) {
    var prev_x = undefined;
    var prev_y = undefined;
    for (var x = 0; x < 95; x++) {
        var y = Math.round(func(x));
        if (!isFinite(y)) {
            prev_x = undefined;
            prev_y = undefined;
            continue;
        }
        if (prev_x !== undefined) {
            this.drawLine(prev_x, prev_y, x, y);
        }
        prev_x = x;
        prev_y = y;
    }
};

Calcugram.prototype.drawCircle = function (h, k, radius) {
    h = Math.round(h);
    k = Math.round(k);
    radius = Math.round(radius);
    var prev_x = undefined;
    var prev_y = undefined;
    for (var i = 0; i <= 360; i++) {
        var t = i * Math.PI / 180;
        var x = Math.round(radius * Math.cos(t) + h);
        var y = Math.round(radius * Math.sin(t) + k);
        if (prev_x !== undefined) {
            this.drawLine(prev_x, prev_y, x, y);
        }
        prev_x = x;
        prev_y = y;
    }
};

