Calcugram.prototype.drawString = function (str, x, y) {
    for (var i = 0; i < str.length; i++) {
        this.drawCharIndex(Calcugram.charToIndex(str.charAt(i)), (x + i), y);
    }
};

Calcugram.prototype.drawCharIndex = function (index, x, y) {
    var char = Calcugram.chars[index];
    for (var i_y = 0; i_y < 7; i_y++) {
        for (var i_x = 0; i_x < 5; i_x++) {
            this.setPixel(i_x + (x * 6), 62 - (i_y + (y * 8)), char[i_y][i_x] != 0);
        }
    }
};

